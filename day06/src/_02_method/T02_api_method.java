package _02_method;

public class T02_api_method {
	// 4칙 연산 +,-,*,/
	
	//메소드 4개
	//return값 있음
	//매개변수 2개
	
	int add(int num1, int num2) {
		return num1+num2;
	}
	
	int minus(int num1, int num2 ) {
		return num1-num2;
	}
	
	int multiply(int num1, int num2) {
		return num1*num2;
	}
	
	double divide(int dou1, int dou2) {
		return dou1/dou2;
	}
}
